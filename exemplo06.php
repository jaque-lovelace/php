<?php

//Condicional com switch

$numero = 3;

switch ($numero) {
    case 1:
        echo "1";
        break;
    
    case 2:
        echo "2";
        break;
        
    case 3:
        echo "3";
        break;
            
    default:
        echo "Opção inválida!";
        break;
}

?>